FIRST, LAST = 0x20, 0x7E

def cols(rows):
    out = []
    for c in range(5):
        byte = 0
        for r in range(7):
            if rows[r][c] == '#':
                byte |= (1 << r)
        out.append(byte)
    return out

def main():
    here = os.path.dirname(os.path.abspath(__file__))
    out_path = os.path.join(here, "..", "..", "components", "ui", "src", "font_oled.cpp")
    out_path = os.path.normpath(out_path)
    lines = []
    lines.append("// GENERATED by tools/oledfont/gen.py — do not edit by hand.")
    lines.append("// Crisp 1bpp 5x7 bitmap font for the mono OLED, column-major: 5 column")
    lines.append("// bytes per glyph (col 0 = left), bit r = row r from the top (bit0 = top).")
    lines.append("// ASCII 0x20..0x7E. The 6th advance column is added by the renderer.")
    lines.append("")
    lines.append('#include "font.h"')
    lines.append("")
    lines.append("namespace pixfrog::ui::detail {")
    lines.append("")
    lines.append(f"const uint8_t kFontOled5x7[{LAST - FIRST + 1}][kFontWidth] = {{")
    for code in range(FIRST, LAST + 1):
        ch = chr(code)
        rows = G.get(ch)
        if rows is None:
            raise SystemExit(f"missing glyph for 0x{code:02X} ({ch!r})")
        b = cols(rows)
        disp = ch if ch not in "\\" else "\\\\"
        lines.append("    { " + ", ".join(f"0x{x:02X}" for x in b) + f" }},  // 0x{code:02X} '{disp}'")
    lines.append("};")
    lines.append("")
    lines.append("const uint8_t* font_oled_for(char c) {")
    lines.append("    if (c < kFontFirstChar || c > kFontLastChar) return kFontOled5x7[0];")
    lines.append("    return kFontOled5x7[static_cast<int>(c) - kFontFirstChar];")
    lines.append("}")
    lines.append("")
    lines.append("}  // namespace pixfrog::ui::detail")
    lines.append("")
    with open(out_path, "w") as f:
        f.write("\n".join(lines))
    print(f"wrote {out_path} ({LAST - FIRST + 1} glyphs)")

if __name__ == "__main__":
    main()
