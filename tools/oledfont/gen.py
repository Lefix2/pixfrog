# oledfont/gen.py — emit the crisp 1bpp 5x7 OLED font table from hand-drawn art.
#
# Source of truth is the GLYPHS dict below: each printable ASCII char maps to 7
# rows of 5 cells ('#' = ink). We emit components/ui/src/font_oled.cpp as a
# column-major table kFontOled5x7[95][5]: 5 column bytes per glyph (col 0 = left),
# bit r = row r counted from the top (bit0 = top row, bits 0..6 used, bit7 = 0).
# The 6th advance column (spacing) is added by the renderer, matching the AA
# font's kFontCellWidth = 6. Glyphs sit in rows 0..6; row 7 is the descender slot.
#
#   python3 tools/oledfont/gen.py
#
# Drawn for the pixel grid (uniform 1px stems) so the mono OLED stays crisp
# instead of thresholding the anti-aliased TFT font. See tools/oledfont/README.md.

import sys, os

# Each glyph: 7 strings, 5 chars wide. '#'=on, anything else=off.
# Descenders (g j p q y , ; etc.) use a 6th/7th low row where noted.
G = {}

